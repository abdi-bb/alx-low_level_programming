/**
 * _sqrt_recursion - function that returns square root of natural number
 * @n: number whose natural square root is to be calculated
 *
 * Return: returns -1 if n has no natural square root
 */
int _sqrt_recursion(int n)
{
	int x = 0;

	if (n == x * x)
	{
		return (x);
	}
	else
	{
		return (-1);
	}
}
